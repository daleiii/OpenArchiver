import { api } from '$lib/server/api';
import type { SystemSettings } from '@open-archiver/types';
import { error, fail } from '@sveltejs/kit';
import type { Actions, PageServerLoad } from './$types';

interface TagFacet {
	tag: string;
	count: number;
}

export const load: PageServerLoad = async (event) => {
	const response = await api('/settings/system', event);

	if (!response.ok) {
		const { message } = await response.json();
		throw error(response.status, message || 'Failed to fetch system settings');
	}

	const systemSettings: SystemSettings = await response.json();

	// Fetch available tags for the default excluded tags dropdown
	let availableTags: TagFacet[] = [];
	try {
		const tagsResponse = await api('/search/facets/tags', event, { method: 'GET' });
		if (tagsResponse.ok) {
			availableTags = await tagsResponse.json();
		}
	} catch {
		// Silently fail - tags will just be empty
	}

	return {
		systemSettings,
		availableTags,
	};
};

export const actions: Actions = {
	default: async (event) => {
		const formData = await event.request.formData();
		const language = formData.get('language');
		const theme = formData.get('theme');
		const supportEmail = formData.get('supportEmail');
		const searchMaxTotalHitsRaw = formData.get('searchMaxTotalHits');
		const searchMaxTotalHitsUnlimited = formData.get('searchMaxTotalHitsUnlimited');
		const defaultExcludedTagsRaw = formData.get('defaultExcludedTags');

		// Parse searchMaxTotalHits: if unlimited checkbox is checked, set to null
		let searchMaxTotalHits: number | null = null;
		if (searchMaxTotalHitsUnlimited !== 'true') {
			const parsed = parseInt(String(searchMaxTotalHitsRaw), 10);
			searchMaxTotalHits = isNaN(parsed) || parsed <= 0 ? null : parsed;
		}

		// Parse defaultExcludedTags: comma-separated string to array
		const defaultExcludedTags = defaultExcludedTagsRaw
			? String(defaultExcludedTagsRaw)
					.split(',')
					.map((t) => t.trim())
					.filter(Boolean)
			: [];

		const body: Partial<SystemSettings> = {
			language: language as SystemSettings['language'],
			theme: theme as SystemSettings['theme'],
			supportEmail: supportEmail ? String(supportEmail) : null,
			searchMaxTotalHits,
			defaultExcludedTags,
		};

		const response = await api('/settings/system', event, {
			method: 'PUT',
			body: JSON.stringify(body),
		});

		if (!response.ok) {
			const { message } = await response.json();
			return fail(response.status, { message: message || 'Failed to update settings' });
		}

		const updatedSettings: SystemSettings = await response.json();

		return {
			success: true,
			settings: updatedSettings,
		};
	},
};
